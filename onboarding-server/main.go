package main

import (
	"encoding/json"
	"fmt"
	"log"
	"net/http"
	"os"
)

func main() {
	port := os.Getenv("PORT")
	if port == "" {
		port = "8080"
	}

	http.HandleFunc("/onboarding/start", onboardingStart)
	http.HandleFunc("/onboarding/stop", onboardingStop)
	
	fmt.Printf("Onboarding server starting on port %s\n", port)
	log.Fatal(http.ListenAndServe(":"+port, nil))
}

func onboardingStart(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "Method not allowed", http.StatusMethodNotAllowed)
		return
	}

	// Basic JSON validation
	var body map[string]interface{}
	if err := json.NewDecoder(r.Body).Decode(&body); err != nil {
		http.Error(w, "Invalid JSON", http.StatusBadRequest)
		return
	}

	// TODO: Add your parameter validation here
	log.Printf("OnboardingStart called with: %+v", body)

	w.Header().Set("Content-Type", "application/json")
	w.WriteHeader(http.StatusOK)
	json.NewEncoder(w).Encode(map[string]string{
		"status":  "success",
		"message": "Onboarding started",
	})
}

